// Function to add numbers
function add(x, y) {
    return x + y;
}

// Function to divise numbers

function divideByTwo(num){
    return num / 2;
}
// Use the different functions discussed above
var sum = add(15, 67);
var average = divideByTwo(sum);

console.log(average);
